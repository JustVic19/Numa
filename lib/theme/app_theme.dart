  static Color getSuccessColor(bool isLight) {
    return isLight ? successLight : successDark;
  }

  /// Helper method to get warning color based on theme
  static Color getWarningColor(bool isLight) {
    return isLight ? warningLight : warningDark;
  }

  /// Helper method to get error color based on theme
  static Color getErrorColor(bool isLight) {
    return isLight ? errorLight : errorDark;
  }

  /// Helper method to get current theme success color from context
  static Color getContextSuccessColor(BuildContext context) {
    final brightness = Theme.of(context).brightness;
    return brightness == Brightness.light ? successLight : successDark;
  }

  /// Helper method to get current theme warning color from context
  static Color getContextWarningColor(BuildContext context) {
    final brightness = Theme.of(context).brightness;
    return brightness == Brightness.light ? warningLight : warningDark;
  }

  /// Helper method to get current theme error color from context
  static Color getContextErrorColor(BuildContext context) {
    final brightness = Theme.of(context).brightness;
    return brightness == Brightness.light ? errorLight : errorDark;
  }

  /// Helper method to check if current theme is light
  static bool isLightTheme(BuildContext context) {
    return Theme.of(context).brightness == Brightness.light;
  }

  /// Helper method to check if current theme is dark
  static bool isDarkTheme(BuildContext context) {
    return Theme.of(context).brightness == Brightness.dark;
  }
